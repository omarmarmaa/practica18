package ejercicio1;

public class MathUtils {
    public static void main(String[] args) {

        int dato1,dato2,resultado;
        dato1=20;
        dato2=10;

        //suma
        resultado=dato1 + dato2;
        System.out.println(dato1+ " + " +dato2 +" = " +resultado);

        //resta
        resultado= dato1 - dato2;
        System.out.println(dato1 + " - " + dato2+ " = " +resultado);

        //producto
        resultado =dato1 * dato2;
        System.out.println(dato1+ " * " +dato2 + " = " +resultado);

        //Cociente
        resultado=dato1/dato2;
        System.out.println(dato1 + " / " + dato2 + " = " +resultado);
    }
}

package practicaExamen.chatgpexamen.While;

//Pide un número n.
//
//Imprime todos los números desde 1 hasta n usando while.
public class ejercicio1 {
    public static void main(String[] args) {
        int n = 5;
        int i = 1;
        while (i <= n) {
            System.out.println(i);
            i++;
        }
    }
}
